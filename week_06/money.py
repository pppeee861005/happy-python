# 印出財務報表：
# 已知：某公司近幾年營業額與利潤如下方表格
# 輸出：請增加「獲利率」一欄，計算並使用格式化字串方法
#       排版並以相同樣式於螢幕印出這份財務報表。
#       其中營業額與利潤要加上千分位符號；
#       獲利率要採百分比表示，精確度到小數點後2位。
#
#
#   年度      營業額       利潤      獲利率
# ==========================================
#    108      1550000	    47895
#    109      2000000	   104600
#    110      2234000	   122200
# ==========================================

data = [
    (108, 1550000, 47895),
    (109, 2000000, 104600),
    (110, 2234000, 122200),
]

print(f"{'年度':>6}{'營業額':>14}{'利潤':>12}{'獲利率':>10}")
print("=" * 46)
for year, revenue, profit in data:
    rate = profit / revenue * 100
    print(f"{year:>6}{revenue:>14,}{profit:>12,}{rate:>9.2f}%")
print("=" * 46)