#  3) 若出現降雨量為負值則應排除，因為不可能發生。
#  4) 輸出的結果中要包括有效紀錄天數、雨天數、期間的總降雨量、
#     以及單日最大降雨量是哪天 (第幾天)？
#######################################################################
# ============================================
# 氣象降雨量統計分析程式
# 提示工程練習 - 蘇格拉底式推導
# ============================================

# 提示詞：建立一個空清單來存放所有有效的每日降雨量
rain_list = []

# 提示詞：使用 while True 無窮迴圈，直到使用者輸入 Q 才停止
while True:
    daily_rain = input("請輸入今日降雨量 (Q 結束): ")
    
    # 提示詞:第一關 — 檢查使用者是否輸入 Q (大小寫都接受),是的話跳出迴圈
    if daily_rain.upper() == "Q":
        break
    
    # 提示詞:第二關 — 將字串轉成浮點數(降雨量可能有小數)
    daily_rain = float(daily_rain)
    
    # 提示詞:第三關 — 排除負值(不可能發生),用 continue 跳過這一輪
    if daily_rain < 0:
        print("⚠️  降雨量不能為負,已排除這筆資料")
        continue
    
    # 提示詞:通過所有檢查,將有效資料加入清單
    rain_list.append(daily_rain)

# ============================================
# 統計分析
# ============================================

# 提示詞:計算雨天數(降雨量大於 0 的天數)
rain_days = 0
for r in rain_list:
    if r > 0:
        rain_days += 1

# 提示詞:計算 4 個統計指標
total_days = len(rain_list)                          # ① 有效紀錄天數
total_rain = sum(rain_list)                          # ③ 總降雨量
max_rain = max(rain_list)                            # 最大降雨量值
max_day = rain_list.index(max_rain) + 1              # ④ 最大降雨量是第幾天

# ============================================
# 輸出結果
# ============================================
print("\n========== 降雨量統計報告 ==========")
print(f"有效紀錄天數: {total_days} 天")
print(f"雨天數      : {rain_days} 天")
print(f"期間總降雨量: {total_rain} mm")
print(f"單日最大降雨: {max_rain} mm (第 {max_day} 天)")
print("====================================")