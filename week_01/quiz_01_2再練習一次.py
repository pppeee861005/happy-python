#__練習二 - 固定數值版本：__

#```python
# [練習二] 固定數值版本
impressions = 2000      # 曝光人數
conversion_rate = 0.05  # 轉化率 (5%)
average_spend = 150     # 平均消費額
total_revenue = impressions * conversion_rate * average_spend
print(f"廣告成效：{total_revenue}元")
print("####################################")



# [練習二] 互動式輸入版本
impressions = int(input("請輸入曝光人數："))
conv_rate = float(input("請輸入預期轉化率 (例如 5 代表 5%)："))
avg_spend = float(input("請輸入顧客平均消費額："))
total_revenue = impressions * (conv_rate / 100) * avg_spend
print(f"廣告成效：{total_revenue}元")
print("####################################")

"""
解釋
__impressions 的意思：__

- 指廣告被顯示的次數
- 在 Facebook 廣告中，代表有多少人看到了你的廣告
- 例如：impressions = 2000 表示有 2000 人看到了你的廣告

__在練習二中的應用：__

- 假設你投放 Facebook 廣告，設定要給 2000 人看
- 這 2000 人就是你的「曝光人數」
- 其中 5% 的人會點擊/轉化（即 2000 × 0.05 = 100 人）
- 這 100 人平均每人消費 150 元
- 最終總營收 = 100 × 150 = 15,000 元

__其他相關術語：__

- __Conversion Rate (轉化率)__：看到廣告後採取行動的百分比
- __Average Spend (平均消費)__：每個轉化顧客的平均消費金額
- __Total Revenue (總營收)__：impressions × conversion_rate × average_spend

這個練習主要是讓你了解廣告投放的基本數據計算方式！

"""

